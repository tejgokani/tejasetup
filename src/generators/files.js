/**
 * @param {import('../config/userConfig.js').UserConfig} config
 */
function serverDependencies(config) {
  /** @type {Record<string, string>} */
  const d = {
    express: '^4.21.2',
    cors: '^2.8.5',
    dotenv: '^16.4.7',
  };
  Object.assign(d, mergeFeatureServerDependencies(config));
  return d;
}

/**
 * @param {import('../config/userConfig.js').UserConfig} config
 * @param {string} scope
 */
function buildRootPackageJson(config, scope) {
  const workspaces = ['shared'];
  if (config.stack.frontend === 'react-vite') workspaces.push('client');
  if (config.stack.backend === 'node-express') workspaces.push('server');

  /** @type {Record<string, string>} */
  const scripts = {};
  const devParts = [];

  if (config.stack.backend === 'node-express') {
    scripts['dev:server'] = 'npm run dev --workspace=server';
    devParts.push('"npm run dev:server"');
  }
  if (config.stack.frontend === 'react-vite') {
    scripts['dev:client'] = 'npm run dev --workspace=client';
    devParts.push('"npm run dev:client"');
  }
  if (devParts.length > 1) {
    scripts.dev = `concurrently ${devParts.join(' ')}`;
  } else if (devParts.length === 1) {
    scripts.dev = devParts[0].replaceAll('"', '');
  }

  if (config.stack.backend === 'node-express') {
    scripts.start = 'npm run start --workspace=server';
  }

  /** @type {Record<string, string>} */
  const devDependencies = {};
  if (devParts.length > 1) {
    devDependencies.concurrently = '^9.1.2';
  }

  /** @type {Record<string, unknown>} */
  const pkg = {
    name: scope,
    version: '1.0.0',
    private: true,
    type: 'module',
    description: `Generated by tejasetup — ${config.projectName}`,
    workspaces,
    scripts,
  };

  if (Object.keys(devDependencies).length) {
    pkg.devDependencies = devDependencies;
  }

  if (isCliLayout(config) && config.stack.cli === 'node') {
    pkg.bin = { [scope]: './bin/cli.js' };
    pkg.dependencies = { commander: '^13.1.0' };
    if (!pkg.scripts.start) pkg.scripts.start = `node bin/cli.js`;
  }

  return pkg;
}

