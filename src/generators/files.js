/**
 * @param {import('../config/userConfig.js').UserConfig} config
 */
function dotenvContent(config) {
  const lines = [
    `PROJECT_NAME=${config.projectName}`,
    `PORT=3001`,
    `VITE_APP_NAME=${config.projectName}`,
    `JWT_SECRET=change-me-in-production`,
    `SQLITE_PATH=./data/app.db`,
    `# DATABASE_URL=postgres://user:pass@localhost:5432/db`,
    ``,
    `# Features: ${flattenConfigFeatures(config).join(', ') || 'none'}`,
  ];
  return lines.join('\n');
}

/**
 * @param {import('../config/userConfig.js').UserConfig} config
 */
function readmeContent(config) {
  const stack = [];
  if (config.stack.frontend) stack.push(`- Frontend: ${config.stack.frontend}`);
  if (config.stack.backend) stack.push(`- Backend: ${config.stack.backend}`);
  if (config.stack.cli) stack.push(`- CLI: ${config.stack.cli}`);

  return `# ${config.projectName}

Generated with **tejasetup**.

## Stack

${stack.join('\n') || '- Custom'}

## Scripts

\`\`\`bash
npm install
${isCliLayout(config) && config.stack.cli ? 'npm start    # or: node bin/cli.js hello\n' : 'npm run dev\n'}
\`\`\`

## Layout

- \`shared/\` — shared constants
${config.stack.frontend === 'react-vite' ? '- `client/` — Vite + React\n' : ''}${config.stack.backend === 'node-express' ? '- `server/` — Express API\n' : ''}${isCliLayout(config) && config.stack.cli ? '- `bin/cli.js` — CLI entry\n' : ''}
`;
}

/**
 * @param {import('../config/userConfig.js').UserConfig} config
 */
export function renderServerEntry(config) {
  const imports = [
    `import 'dotenv/config';`,
    `import express from 'express';`,
    `import cors from 'cors';`,
    `import { healthRouter } from './routes/health.js';`,
  ];
  if (hasScaffoldFeature(config, 'auth')) {
    imports.push(`import { authRouter } from './routes/auth.js';`);
  }
  if (hasScaffoldFeature(config, 'database')) {
    imports.push(`import { dbRouter } from './routes/db.js';`);
  }
  if (hasScaffoldFeature(config, 'logging')) {
    imports.push(`import { logger } from './lib/logger.js';`);
  }

  const body = [
    `const app = express();`,
    `app.use(cors({ origin: true }));`,
    `app.use(express.json());`,
  ];

  if (hasScaffoldFeature(config, 'logging')) {
    body.push(
      `app.use((req, _res, next) => { void logger.info(\`\${req.method} \${req.url}\`, { project: process.env.PROJECT_NAME }); next(); });`,
    );
  }

  body.push(`app.use('/api', healthRouter);`);
  if (hasScaffoldFeature(config, 'auth')) {
    body.push(`app.use('/api/auth', authRouter);`);
  }
  if (hasScaffoldFeature(config, 'database')) {
    body.push(`app.use('/api/db', dbRouter);`);
  }

  body.push(`const port = Number(process.env.PORT) || 3001;`);
  body.push(
    `app.listen(port, () => { console.log(\`[${config.projectName}] API http://localhost:\${port}\`); });`,
  );

  return `${imports.join('\n')}\n\n${body.join('\n')}\n`;
}

function healthRouteContent() {
  return `import { Router } from 'express';

export const healthRouter = Router();

healthRouter.get('/health', (_req, res) => {
  res.json({
    ok: true,
    project: process.env.PROJECT_NAME,
    time: new Date().toISOString(),
  });
});
`;
}

/**
 * @param {Map<string, string>} map
 * @param {import('../config/userConfig.js').UserConfig} config
 */
function assignGeneratedFeatureFiles(map, config) {
  const authClientFile = useTypeScript(config)
    ? 'client/src/lib/auth.ts'
    : 'client/src/lib/auth.js';
  if (hasScaffoldFeature(config, 'auth')) {
    if (config.stack.backend === 'node-express') {
      map.set('server/src/middleware/jwt.js', serverAuthMiddleware());
      map.set('server/src/routes/auth.js', serverAuthRoutes(config));
    }
    if (config.stack.frontend === 'react-vite') {
      map.set(authClientFile, clientAuthStub());
    }
  }
  if (hasScaffoldFeature(config, 'database')) {
    if (config.stack.backend === 'node-express') {
      map.set('server/src/db/index.js', serverDbModule(config));
      map.set('server/src/routes/db.js', serverDbRoutes());
    } else {
      map.set('shared/src/db/README.md', sharedDbReadme());
    }
  }
  if (hasScaffoldFeature(config, 'logging')) {
    if (config.stack.backend === 'node-express') {
      map.set('server/src/lib/logger.js', serverLoggerModule());
    } else {
      map.set('shared/src/logger.js', sharedLoggerModule());
    }
  }
}

/**
 * @param {Map<string, string>} map
 * @param {import('../config/userConfig.js').UserConfig} config
 * @param {string} scope
 */
function putShared(map, config, scope) {
  const ts = useTypeScript(config);
  const entry = ts ? './src/index.ts' : './src/index.js';
  map.set(
    'shared/package.json',
    `${JSON.stringify(
      {
        name: `@${scope}/shared`,
        version: '1.0.0',
        private: true,
        type: 'module',
        main: entry,
        exports: { '.': entry },
      },
      null,
      2,
    )}\n`,
  );
  map.set(
    ts ? 'shared/src/index.ts' : 'shared/src/index.js',
    `export const projectName = ${JSON.stringify(config.projectName)};\nexport const features = ${JSON.stringify(config.features)};\n`,
  );
}

